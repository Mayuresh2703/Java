public class literals {
    public static void main(String[] args) {
        int num1 = 0b101;
//        prints output in binary format
        System.out.println(num1);
//        for hexadecimal numbers
        int num2 = 0x7E;
        System.out.println(num2);

        double num4 = 12e10;
        System.out.println(num4);

    }
}
